#include "3-calc.h"
#include <stdio.h>

/**
 * op_add - addition function
 * @a: num1
 * @b: num2
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - differenece
 * @a: num1
 * @b: num2
 * Return: difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplication
 * @a: num1
 * @b: num2
 * Return: multiplication
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - division
 * @a: num1
 * @b: num2
 * Return: division
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulation
 * @a: num1
 * @b: num2
 * Return: mod
 */
int op_mod(int a, int b)
{
	return (a % b);
}
